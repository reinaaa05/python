class Greeting:
    def __init__(self):
        self.msg = "hello"
        self.target = "paiza"

    def say_hello(self):
        print(self.msg + " " + self.target)

class Hello(Greeting):
    # ここにオーバライドするメソッドを記述する
    def __init__(self,target):
        self.msg = "hello"
        self.target = target


player = Hello("python")
player.say_hello()
